public class App {
    public static void main(String[] args) {
        int a = 10;
        int b = 7;
        System.out.println(sum(a,b));
        System.out.println(sub(a,b));
    }
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int sub(int a, int b) {
        return a - b;
    }
}
